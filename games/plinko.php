    <script>
        $(document).ready(function() {
            // Load win rate for plinko
            $.get(getApiPath('getWinRates') + '&game=plinko', function(data) {
                if (data.success && data.winRate) {
                    $('#winRate').text(data.winRate.rate || 0);
                    $('#gamesPlayed').text(data.winRate.total || 0);
                    $('#wins').text(data.winRate.wins || 0);
                    const netWinLoss = data.winRate.netWinLoss || 0;
                    const netWinLossText = netWinLoss >= 0 ? '$' + netWinLoss.toFixed(2) : '-$' + Math.abs(netWinLoss).toFixed(2);
                    $('#netWinLoss').text(netWinLossText).css('color', netWinLoss >= 0 ? '#28a745' : '#dc3545');
                } else {
                    console.error('Failed to load stats:', data);
                    $('#winRate').text('0');
                    $('#gamesPlayed').text('0');
                    $('#wins').text('0');
                    $('#netWinLoss').text('$0.00').css('color', '#666');
                }
            }, 'json').fail(function(xhr, status, error) {
                console.error('Error loading stats:', status, error);
                $('#winRate').text('0');
                $('#gamesPlayed').text('0');
                $('#wins').text('0');
                $('#netWinLoss').text('$0.00').css('color', '#666');
            });
            
            // Load and display multipliers
            $.get(getApiPath('getSettings'), function(data) {
                if (data.success && data.settings.plinko_multipliers && Array.isArray(data.settings.plinko_multipliers)) {
                    const multipliers = data.settings.plinko_multipliers;
                    const slotNames = ['Left Edge', 'Near Left', 'Left-Mid', 'Left-Center', 'Center', 'Right-Center', 'Right-Mid', 'Near Right', 'Right Edge'];
                    
                    let html = '';
                    for (let i = 0; i < multipliers.length; i++) {
                        html += `<tr>
                            <td>Slot ${i} (${slotNames[i]})</td>
                            <td>${multipliers[i].toFixed(1)}x</td>
                        </tr>`;
                    }
                    $('#plinkoMultipliersBody').html(html);
                }
            }, 'json');
        });
    </script>
    
